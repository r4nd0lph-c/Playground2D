using UnityEngine;

namespace TimeSystem
{
    public class TimeManager : MonoBehaviour
    {
        public static TimeManager Instance;

        private void Awake()
        {
            if (Instance == null)
            {
                Instance = this;
                DontDestroyOnLoad(gameObject);
            }
            else
            {
                Destroy(gameObject);
            }
        }
    }
}